class ArrayExample
{
    public static void main(String[] args)
    {
        int[] age = {12, 4, 5, 2, 5}; //initializing array

        //printing elements of array
        for (int i = 0; i < 5; ++i) {
            System.out.println("Element at index " + i +": " + age[i]);
        }
    }
}